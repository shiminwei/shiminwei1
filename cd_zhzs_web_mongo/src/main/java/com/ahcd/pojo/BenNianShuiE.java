package com.ahcd.pojo;

public class BenNianShuiE {
	private Integer shouRuYuqi; //本年收入预期
	private Integer shouRu;//本年实际收入
	private Integer shouRuJinDu;//收入进度
	
	public Integer getShouRuYuqi() {
		return shouRuYuqi;
	}
	public void setShouRuYuqi(Integer shouRuYuqi) {
		this.shouRuYuqi = shouRuYuqi;
	}
	public Integer getShouRu() {
		return shouRu;
	}
	public void setShouRu(Integer shouRu) {
		this.shouRu = shouRu;
	}
	public Integer getShouRuJinDu() {
		return shouRuJinDu;
	}
	public void setShouRuJinDu(Integer shouRuJinDu) {
		this.shouRuJinDu = shouRuJinDu;
	}
}
